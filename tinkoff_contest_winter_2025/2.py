"""В самый что ни на есть обычный день Борис решил, что на протяжении следующих n дней он будет покупать своей маме букет ровно из трех цветов. Там, где живет Борис, существует всего лишь один магазин цветов, но зато в нем широкий ассортимент: для каждого i от 0 до 10^100 в магазине есть ровно один уникальный цветок, стоящий 2^i бурлей. К тому же в магазин ежедневно довозят цветы, которые были выкуплены.
В i-й день у Бориса есть ai бурлей, которые он готов потратить на букет. Борис хочет купить как можно более дорогой букет. Для каждого из n дней определите, за какую стоимость Борис купит букет, или сообщите, что на его деньги невозможно купить никакой букет из трех цветов.

Формат входных данных
Первая строка содержит число n (1<=n<=10^5)  — количество дней, в течение которых Борис планирует покупать букеты.
i-я из следующих n строк содержит число ai (1<=ai<=10^18) — количество бурлей, которое есть у Бориса в i-й день.

Формат выходных данных
Для каждого a_i в отдельной строке выведите, сколько бурлей Борис потратит на букет в i-й день, или -1, если он не может купить никакой букет ровно из трех цветов.

Пример данных:
Ввод
3
15
67
5

Вывод
14
67
-1

Комментарий к примеру
в первый день Борис может купить цветы стоимостей 2^1, 2^2 и 2^3;
во второй день Борис может купить цветы стоимостей 2^0, 2^1 и 2^6;
в третий день Борис не может купить цветы таким образом, чтобы составить букет из трех цветов и уложиться в сумму в 5 бурлей."""


def find_max_bouquet(money):
    # Находим максимальную возможную степень двойки
    max_power = 0
    while (1 << max_power) <= money:
        max_power += 1
    
    # Перебираем все возможные комбинации трёх различных степеней
    max_sum = -1
    for i in range(max_power):
        for j in range(i + 1, max_power):
            for k in range(j + 1, max_power):
                current_sum = (1 << i) + (1 << j) + (1 << k)
                if current_sum <= money:
                    max_sum = max(max_sum, current_sum)
    
    return max_sum

# Читаем входные данные
n = int(input())
for _ in range(n):
    money = int(input())
    print(find_max_bouquet(money))